//Operadores para comparar:
    3 == "3" //compara solo los valores (en este caso devuelve "true")

    3 === "3" //compara los valores y el tipo de dato (devuelve "false")

    5 < 3 //Compara y valida si 5 es menor a 3 (devuelve "false")
    5 > 3 //Compara y valida si 5 es mayor a 3 (devuelve "true")
    5 <= 6 //Compara y valida si 5 es menor o igual a 6 (devuelve "true")
    5 >= 6 //Compara y valida si 5 es mayor o igual a 6 (devuelve "false")

    a && b //deben cumplirse las dos condiciones para que devuelva "true", si alguna es falsa, el resultado sera "false"
    a || b //debe cumplirse alguna de las dos condiciones para que el resultado sea "true", solo si ambas son falsas retornara "false"

